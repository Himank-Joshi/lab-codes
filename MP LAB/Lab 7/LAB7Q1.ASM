DATA SEGMENT
A DB 10H,20H,30H,40H,50H,60H,70H,80H,90H,99H
KEY DB 60H
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:MOV AX,DATA
      MOV DS,AX
      MOV AL,KEY
      LEA SI,A
      MOV CX,10

  UP:CMP AL,[SI]
     JE FOUND
     INC SI
     LOOP UP

 NOTFOUND:
        JMP EXIT

 FOUND:MOV DX,11
 EXIT:
     MOV AH,4CH
     INT 21H
     CODE ENDS
     END START
